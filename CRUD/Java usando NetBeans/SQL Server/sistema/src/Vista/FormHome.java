    public void cargarReporte(String reporte) {
        Conexion conexion_now = new Conexion();

        conexion_now.abrir();

        try {
            JasperReport reporteJasper = JasperCompileManager.compileReport(reporte);
            JasperPrint mostrarReporte = JasperFillManager.fillReport(reporteJasper, null, conexion_now.retornar());
            JasperViewer.viewReport(mostrarReporte, false);

        } catch (Exception ex) {
            Logger.getLogger(FormHome.class.getName()).log(Level.SEVERE, null, ex);
        }

        conexion_now.cerrar();
    }

